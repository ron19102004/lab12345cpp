#include <iostream>
using namespace std;
int main()
{
    float r; //khai báo biến 
    cout << "Enter radius r = ";
    cin >> r;
    cout << "perimeter of circle is: " << 2 * 3.14 * r << endl;// xuất màn hình câu trong ngoặc kép kèm kết quả
    cout << "area of circle is: " << 3.14 * r * r << endl;// xuất màn hình câu trong ngoặc kép kèm kết quả
    return 0;
}